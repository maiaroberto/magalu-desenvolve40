# Faça um programa que pergunte ao usuário quanto ele ganha por hora e a quantidade de horas trabalhadas no mês. 
# Calcule o total do salário do mês.

valorHora = int(input("Digite quanto você ganha por hora: ")) 
horasTrabalhadas = int(input("Digite número de horas trabalhadas no mês: ")) 
salario = valorHora * horasTrabalhadas 
print("Salario no mês= ", salario)
